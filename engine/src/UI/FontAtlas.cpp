bool FontAtlas::Load(const std::string& ttfPath, float pixelHeight) {
    std::ifstream file(ttfPath, std::ios::binary | std::ios::ate);
    if (!file.is_open()) {
        VE_ENGINE_ERROR("FontAtlas: failed to open '{}'", ttfPath);
        return false;
    }

    auto size = file.tellg();
    file.seekg(0);
    std::vector<unsigned char> fontData(static_cast<size_t>(size));
    file.read(reinterpret_cast<char*>(fontData.data()), size);

    BuildAtlas(fontData.data(), pixelHeight);
    return m_AtlasTexture != nullptr;
}

bool FontAtlas::LoadDefault(float pixelHeight) {
    m_PixelHeight = pixelHeight;
    float scale = pixelHeight / static_cast<float>(EMBED_GLYPH_H);
    int scaledW = static_cast<int>(EMBED_GLYPH_W * scale + 0.5f);
    int scaledH = static_cast<int>(EMBED_GLYPH_H * scale + 0.5f);

    int glyphCount = EMBED_LAST_CHAR - EMBED_FIRST_CHAR + 1;
    int cols = 16;
    int rows = (glyphCount + cols - 1) / cols;
    int atlasW = cols * scaledW;
    int atlasH = rows * scaledH;

    // Ensure power-of-two-ish for GPU friendliness
    // Round up to next multiple of 4
    atlasW = (atlasW + 3) & ~3;
    atlasH = (atlasH + 3) & ~3;

    std::vector<uint32_t> pixels(atlasW * atlasH, 0);

    for (int ci = 0; ci < glyphCount; ci++) {
        int col = ci % cols;
        int row = ci / cols;
        int baseX = col * scaledW;
        int baseY = row * scaledH;

        const uint8_t* bmp = kEmbeddedFontBitmap[ci];

        // Rasterize with nearest-neighbor scaling
        for (int py = 0; py < scaledH && (baseY + py) < atlasH; py++) {
            int srcRow = static_cast<int>(py / scale);
            if (srcRow >= EMBED_GLYPH_H) srcRow = EMBED_GLYPH_H - 1;
            uint8_t rowBits = bmp[srcRow];

            for (int px = 0; px < scaledW && (baseX + px) < atlasW; px++) {
                int srcCol = static_cast<int>(px / scale);
                if (srcCol >= EMBED_GLYPH_W) srcCol = EMBED_GLYPH_W - 1;

                bool on = (rowBits >> (7 - srcCol)) & 1;
                if (on) {
                    pixels[(baseY + py) * atlasW + (baseX + px)] = 0xFFFFFFFF;
                }
            }
        }

        // Fill glyph info
        char c = static_cast<char>(EMBED_FIRST_CHAR + ci);
        auto& g = m_Glyphs[static_cast<int>(c)];
        g.U0 = static_cast<float>(baseX) / atlasW;
        g.V0 = static_cast<float>(baseY) / atlasH;
        g.U1 = static_cast<float>(baseX + scaledW) / atlasW;
        g.V1 = static_cast<float>(baseY + scaledH) / atlasH;
        g.Width = static_cast<float>(scaledW);
        g.Height = static_cast<float>(scaledH);
        g.BearingX = 0.0f;
        g.BearingY = static_cast<float>(scaledH);
        g.Advance = static_cast<float>(scaledW);
    }

    m_LineHeight = static_cast<float>(scaledH);
    m_AtlasTexture = Texture2D::Create(atlasW, atlasH, pixels.data());
    VE_ENGINE_INFO("FontAtlas: built-in font loaded ({}x{} atlas, scale={:.1f})", atlasW, atlasH, scale);
    return true;
}

void FontAtlas::BuildAtlas(const unsigned char* fontData, float pixelHeight) {
    m_PixelHeight = pixelHeight;

    stbtt_fontinfo info;
    if (!stbtt_InitFont(&info, fontData, 0)) {
        VE_ENGINE_ERROR("FontAtlas: stbtt_InitFont failed");
        return;
    }

    float fontScale = stbtt_ScaleForPixelHeight(&info, pixelHeight);
    int ascent, descent, lineGap;
    stbtt_GetFontVMetrics(&info, &ascent, &descent, &lineGap);
    m_LineHeight = (ascent - descent + lineGap) * fontScale;

    // First pass: measure all glyphs to compute atlas size
    struct GlyphTemp {
        int w, h, xoff, yoff;
        int advW, lsb;
        unsigned char* bitmap = nullptr;
    };
    std::array<GlyphTemp, 128> temps{};

    int totalArea = 0;
    int maxH = 0;
    for (int c = 32; c < 127; c++) {
        auto& gt = temps[c];
        gt.bitmap = stbtt_GetCodepointBitmap(&info, 0, fontScale, c,
                                              &gt.w, &gt.h, &gt.xoff, &gt.yoff);
        stbtt_GetCodepointHMetrics(&info, c, &gt.advW, &gt.lsb);
        totalArea += (gt.w + 2) * (gt.h + 2); // padding
        maxH = std::max(maxH, gt.h);
    }

    // Compute atlas dimensions
    int atlasW = 1;
    while (atlasW * atlasW < totalArea * 2) atlasW *= 2;
    int atlasH = atlasW;

    std::vector<uint32_t> pixels(atlasW * atlasH, 0);

    // Pack glyphs row by row (simple shelf packing)
    int curX = 1, curY = 1, rowH = 0;
    for (int c = 32; c < 127; c++) {
        auto& gt = temps[c];
        if (gt.w == 0 && gt.h == 0) {
            m_Glyphs[c].Advance = gt.advW * fontScale;
            continue;
        }

        if (curX + gt.w + 1 >= atlasW) {
            curX = 1;
            curY += rowH + 1;
            rowH = 0;
        }

        // Copy glyph bitmap to atlas
        for (int y = 0; y < gt.h; y++) {
            for (int x = 0; x < gt.w; x++) {
                uint8_t alpha = gt.bitmap[y * gt.w + x];
                if (alpha > 0) {
                    int px = curX + x;
                    int py = curY + y;
                    if (px < atlasW && py < atlasH)
                        pixels[py * atlasW + px] = (alpha << 24) | 0x00FFFFFF;
                }
            }
        }

        auto& g = m_Glyphs[c];
        g.U0 = static_cast<float>(curX) / atlasW;
        g.V0 = static_cast<float>(curY) / atlasH;
        g.U1 = static_cast<float>(curX + gt.w) / atlasW;
        g.V1 = static_cast<float>(curY + gt.h) / atlasH;
        g.Width = static_cast<float>(gt.w);
        g.Height = static_cast<float>(gt.h);
        g.BearingX = static_cast<float>(gt.xoff);
        g.BearingY = static_cast<float>(-gt.yoff); // stb gives negative yoff
        g.Advance = gt.advW * fontScale;

        curX += gt.w + 1;
        rowH = std::max(rowH, gt.h);

        stbtt_FreeBitmap(gt.bitmap, nullptr);
    }

    m_AtlasTexture = Texture2D::Create(atlasW, atlasH, pixels.data());
    VE_ENGINE_INFO("FontAtlas: TTF loaded ({}x{} atlas, {:.0f}px)", atlasW, atlasH, pixelHeight);
}

const GlyphInfo& FontAtlas::GetGlyph(char c) const {
    int idx = static_cast<int>(static_cast<unsigned char>(c));
    if (idx < 32 || idx > 126) idx = '?';
    return m_Glyphs[idx];
}

float FontAtlas::MeasureTextWidth(const std::string& text) const {
    float w = 0.0f;
    for (char c : text)
        w += GetGlyph(c).Advance;
    return w;
}

std::shared_ptr<FontAtlas> FontAtlas::Create(const std::string& ttfPath, float pixelHeight) {
    auto atlas = std::make_shared<FontAtlas>();
    if (!atlas->Load(ttfPath, pixelHeight)) return nullptr;
    return atlas;
}

std::shared_ptr<FontAtlas> FontAtlas::CreateDefault(float pixelHeight) {
    auto atlas = std::make_shared<FontAtlas>();
    atlas->LoadDefault(pixelHeight);
    return atlas;
}

// ── FontLibrary ──────────────────────────────────────────────────────

std::unordered_map<std::string, std::shared_ptr<FontAtlas>>& FontLibrary::GetFonts() {
    static std::unordered_map<std::string, std::shared_ptr<FontAtlas>> s_Fonts;
    return s_Fonts;
}

void FontLibrary::Register(const std::string& name, const std::shared_ptr<FontAtlas>& font) {
    GetFonts()[name] = font;
}

std::shared_ptr<FontAtlas> FontLibrary::Get(const std::string& name) {
    auto& fonts = GetFonts();
    auto it = fonts.find(name);
    return it != fonts.end() ? it->second : nullptr;
}

bool FontLibrary::Exists(const std::string& name) {
    return GetFonts().count(name) > 0;
}

void FontLibrary::Clear() {
    GetFonts().clear();
}

std::shared_ptr<FontAtlas> FontLibrary::GetDefault() {
    auto& fonts = GetFonts();
    auto it = fonts.find("Default");
    if (it != fonts.end()) return it->second;
    auto def = FontAtlas::CreateDefault(32.0f);
    fonts["Default"] = def;
    return def;
}

